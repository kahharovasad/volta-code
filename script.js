var shareofVoice = "100%";
var flight = "4 Weeks";

function calculation() {
  const matching = data.filter(
    (v) =>
      v.campaignSize === budget &&
      v.SOV === shareofVoice &&
      v.campaignLength === flight
  );
  if (matching.length > 0) {
    $("#co2-offset").text(matching[0]["CO2 Offset"]);
    $("#gallons-saved").text(matching[0]["Gallons of Gas Saved"]);
    $("#miles-delivered").text(matching[0]["Miles Delivered"]);
  }
}

$(document).ready(function () {
  $("#budget").change(function () {
    budget = $(this).val();
    calculation();
  });
  $("#share").change(function () {
    shareofVoice = $(this).val();
    calculation();
  });
  $("#flight").change(function () {
    flight = $(this).val();
    calculation();
  });
  setTimeout(() => {
    calculation();
  }, 1000);
});
